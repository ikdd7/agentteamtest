"""에이전트 팀 정의 (전사 직군).

각 전문 에이전트는 이름, 한 줄 설명, 시스템 프롬프트, 작업에 적합한 effort
수준으로 구성된다. 모든 에이전트는 '분석 먼저, 사고흐름을 드러내는' 공통
원칙(SHARED_PRINCIPLES)을 공유하되 각자의 직군에 집중한다.

코디네이터(orchestrator)가 delegate 도구로 위임할 수 있는 전체 명단은
이 파일의 TEAM 으로 노출된다.
"""

from __future__ import annotations

from dataclasses import dataclass

# 팀 전체가 사용하는 기본 모델. 최신·최상위 Opus 모델을 기본값으로 둔다.
DEFAULT_MODEL = "claude-opus-4-8"

# 모든 에이전트가 공유하는 작업 원칙. 각 시스템 프롬프트 앞에 붙는다.
SHARED_PRINCIPLES = (
    "공통 원칙:\n"
    "- 답을 내기 전에 문제를 먼저 분석합니다. 핵심 가정과 제약을 명시합니다.\n"
    "- 결론을 먼저 제시하고 근거를 뒤에 붙입니다.\n"
    "- 추측과 사실을 구분하고, 불확실한 부분은 그렇다고 밝힙니다.\n"
    "- 당신의 직군 범위에 집중하고, 다른 직군의 일은 그 담당에게 넘깁니다.\n"
)


@dataclass(frozen=True)
class Agent:
    """전문 에이전트 한 명의 페르소나."""

    name: str
    description: str
    role: str  # 직군 분류 (기획/개발/검증/디자인/마케팅/운영/분석)
    _system: str
    effort: str = "medium"
    model: str = DEFAULT_MODEL

    @property
    def system(self) -> str:
        """공통 원칙 + 직군별 지침을 합친 최종 시스템 프롬프트."""
        return f"{SHARED_PRINCIPLES}\n{self._system}"


